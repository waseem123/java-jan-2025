public class UnaryOperators {
    public static void main(String[] args) {
        int x = 100;
        System.out.println(x);
        x++; // x = x + 1   101
        System.out.println(x);
        x--; // x = x - 1   100
        System.out.println(x);

        System.out.println(++x);
        System.out.println(x++);
        System.out.println(x);

        System.out.println(x--);
        System.out.println(--x);

        System.out.println(++x);
    }
}
