public class ArithmeticOperators {
    public static void main(String[] args) {
        int n1 = 150;
        int n2 = 50;

        System.out.println(n1 + n2);
        System.out.println(n1 - n2);
        System.out.println(n1 * n2);
        
        System.out.println(n1 / n2);
        System.out.println(n1 % n2);
    }
}

