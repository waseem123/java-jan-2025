public class AssignmentOperators {
    public static void main(String[] args) {
        int x = 100;  // 100
        int y = x + 10; // 110
        System.out.println(x);
        System.out.println(y);

        x += 5; // x = x + 5
        System.out.println(x);

        x -= 10;
        System.out.println(x);

        y *= 2;
        System.out.println(y);

        y /= 2;
        System.out.println(y);
    }
}
